import { UUID, WithIdAndLabelsModel, WithIdAndLabelsPersist } from './common.ts'
import { PersonalNameModel } from './person.ts'

// ////////////////////////////////////////////////////////////////////////////////////////////////
// Model
// ////////////////////////////////////////////////////////////////////////////////////////////////

export class ContactModel extends WithIdAndLabelsModel {
    name: PersonalNameModel
    aliases: PersonalNameModel[]

    constructor() {
        super()
        this.name = new PersonalNameModel()
        this.aliases = []
    }
}

// ////////////////////////////////////////////////////////////////////////////////////////////////
// Persist
// ////////////////////////////////////////////////////////////////////////////////////////////////

export interface ContactPersist extends WithIdAndLabelsPersist {
    name: UUID
    aliases: UUID[]
}
